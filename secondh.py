"""
Реализуйте функцию, которая принимает матрицу (список списков) и
возвращает матрицу, повернутую на 90 градусов по часовой стрелке. 
Используйте функции zip(), list() и reversed()
"""
def rotate_matrix(matrix):
    """
    Функция, которая поворачивает матрицу на 90 градусов по часовой стрелке.
    
    Аргументы:
    matrix (list of lists): матрица, которую нужно повернуть
    
    Возвращает:
    list of lists: повернутая матрица
    """
    # Используем функцию zip() для транспонирования матрицы
    transposed = list(zip(*matrix))
    
    # Используем функцию reversed() для разворота строк матрицы
    rotated = [list(reversed(row)) for row in transposed]
    
    return rotated
matrix = [[1, 2, 3], [4, 5, 6], [7, 8, 9]]

print(rotate_matrix(matrix))
# Output: [[7, 4, 1], [8, 5, 2], [9, 6, 3]]
