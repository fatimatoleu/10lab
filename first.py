"""
Берем 10 встроенных функций и пишем программу в согласовании
друг с другом. Необходимо чтобы все функции между собой были
связанные, программа имела логическое начало и конец. Функции не
должны были использоваться нами ранее.
"""
# Программа, которая использует встроенные функции Python и связывает их между собой

def main():
    # Создание списка чисел
    numbers = [2, 3, 4, 5, 6, 7, 8]

    # Использование функции filter для фильтрации четных чисел из списка
    even_numbers = list(filter(lambda x: x % 2 == 0, numbers))
    print("Список четных чисел:", even_numbers)

    # Использование функции map для возведения в квадрат всех чисел в списке
    squared_numbers = list(map(lambda x: x**2, numbers))
    print("Список чисел, возведенных в квадрат:", squared_numbers)

    # Использование функции divmod для деления первого числа из списка на второе
    quotient, remainder = divmod(numbers[0], numbers[1])
    print("Частное и остаток от деления {0} на {1}:".format(numbers[0], numbers[1]), quotient, remainder)

    # Использование функций bin, oct и hex для вывода восьмеричного, двоичного и шестнадцатеричного представления числа
    my_number = 42
    print("Восьмеричное представление числа {0}:".format(my_number), oct(my_number))
    print("Двоичное представление числа {0}:".format(my_number), bin(my_number))
    print("Шестнадцатеричное представление числа {0}:".format(my_number), hex(my_number))

    # Использование функции abs для получения абсолютного значения разности первых двух чисел из списка
    absolute_difference = abs(numbers[0] - numbers[1])
    print("Абсолютное значение разности {0} и {1}:".format(numbers[0], numbers[1]), absolute_difference)

    # Использование функции hash для получения хэш-кода объекта
    my_string = "hello world"
    my_hash = hash(my_string)
    print("Хэш-код строки '{0}':".format(my_string), my_hash)

if __name__ == '__main__':
    main()

    """
    Эта программа создает список чисел, фильтрует его, возведет числа в квадрат, делит первое число на второе с помощью функции divmod, 
выводит восьмеричное, двоичное и шестнадцатеричное представления числа, 
получает абсолютное значение разности первых двух чисел и получает хэш-код строки.
Она использует функции filter, map, divmod, bin, oct, hex, abs, 
hash и связывает их между собой для выполнения задачи.
"""